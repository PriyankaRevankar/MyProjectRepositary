package main.java.com.mindtree.campusmindstracks.exception.DaoException;

public class NoTracksException extends TrackDaoException{

	public NoTracksException() {
		super();
		
	}

	public NoTracksException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public NoTracksException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public NoTracksException(String message) {
		super(message);
		
	}

	public NoTracksException(Throwable cause) {
		super(cause);
	}
	

}
